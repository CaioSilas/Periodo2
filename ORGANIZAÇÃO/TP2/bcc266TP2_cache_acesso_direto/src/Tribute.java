
public class Tribute {
	
	private String title;
    private String spider;
    private String company;
    private String date;
    private String jobID;
    private String correct_error_warning_code;
    private String jobReturnMessage;
    private String tributeValue;

    public Tribute() {
        
    }
        
    private String type;
    public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSpider() {
		return spider;
	}

	public void setSpider(String spider) {
		this.spider = spider;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getJobID() {
		return jobID;
	}

	public void setJobID(String jobID) {
		this.jobID = jobID;
	}

	public String getCorrect_error_warning_code() {
		return correct_error_warning_code;
	}

	public void setCorrect_error_warning_code(String correct_error_warning_code) {
		this.correct_error_warning_code = correct_error_warning_code;
	}

	public String getJobReturnMessage() {
		return jobReturnMessage;
	}

	public void setJobReturnMessage(String jobReturnMessage) {
		this.jobReturnMessage = jobReturnMessage;
	}

	public String getTributeValue() {
		return tributeValue;
	}

	public void setTributeValue(String tributeValue) {
		this.tributeValue = tributeValue;
	}

	
}
